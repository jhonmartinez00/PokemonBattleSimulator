public void player1damageCalculation(){
        Random critChance = new Random();
        int critRate=critChance.nextInt(100);
        int totalDamage = ((2*player1Info[3])/5+2)*(player1moveDamage[moveUsed]*(player1Info[1]/player2Info[2]))/50+2;
        int critDamage = totalDamage*2;
        if(critRate <= 10){
            player2Info[0]-=critDamage;
            player1lowerText.setText("Critical Hit! "+player2PokemonName+" takes "+critDamage+" damage.");
        }else{  
            player2Info[0]-=totalDamage;
            player1lowerText.setText(player2PokemonName+" takes "+totalDamage+" damage.");
        }
            enemyHealth.setText(Integer.toString(player2Info[0]));
            player1upperText.setText(player1PokemonName+" uses "+player1moveList[moveUsed]);
     
        p1PokemonHealth.setValue(player1Info[0]);
        p2PokemonHealth.setValue(player2Info[0]);
        
        player2Turn();
    }
