    public void player1Turn(){        
        
        if(player1Info[0] <= 0){
            player1upperText.setText(player1PokemonName+" fainted.");
            player1lowerText.setText("You Lose");
            player2upperText.setText(player1PokemonName+" fainted.");
            player2lowerText.setText("You win!");
            player1status.setText("Lose");
            player2status.setText("Win");
            player1btn1.setEnabled(false);
            player1btn2.setEnabled(false);
            player1btn3.setEnabled(false);
            player1btn4.setEnabled(false);
            Health.setText("0");
            
        }else if(player2Info[0] <= 0){
            player1upperText.setText(player2PokemonName+" fainted.");
            player1lowerText.setText("You win!");
            player2upperText.setText(player2PokemonName+" fainted.");
            player2lowerText.setText("You lose!");
            player1status.setText("Win");
            player2status.setText("Lose");
            player1btn1.setEnabled(false);
            player1btn2.setEnabled(false);
            player1btn3.setEnabled(false);
            player1btn4.setEnabled(false);
            enemyHealth.setText("0");
        }else{            
            player1btn1.setText("FIGHT");
            player1btn2.setText("BAG");
            player1btn3.setText("POKEMON");
            player1btn4.setText("RUN");
        player1btn1.setEnabled(true);
        player1btn2.setEnabled(true);
        player1btn3.setEnabled(true);
        player1btn4.setEnabled(true);
        player1status.setText("Thinking ...");
        player2status.setText("Waiting ...");
        player1upperText.setText("What will "+player1PokemonName+" do?");
        player1lowerText.setText("");
        }
    }
